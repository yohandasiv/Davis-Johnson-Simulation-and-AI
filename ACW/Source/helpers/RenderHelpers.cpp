void OpenGLRenderHelpers::Render(const Ball* pBall) const
{
    // radius of model is 0.5
    // for a radius of 1 we need to scale by double
    glm::mat4 translate = glm::translate(glm::mat4(1.f), pBall->GetPosition());
    glm::mat4 model = glm::scale(translate, glm::vec3(2 * pBall->GetRadius()));

    RenderSphere(pBall->GetColour(), model);
}

void OpenGLRenderHelpers::Render(const Pipe* pPipe) const
{
    // NOTE this is presented as an example of rendering "Game Entities" 
    // This does not take into account the size of the cylinder model 
    // which has a radius of 0.5 and a height of 1 with the origin at the centre

    glm::mat4 model = pPipe->GetModel();

    // scale based on pipe size
    glm::vec3 scale(pPipe->m_BaseRadius, pPipe->m_BaseHeight, pPipe->m_BaseRadius);
    model = glm::scale(model, scale);

    // translate cylinder model to be level with ground
    model = glm::translate(model, glm::vec3(0.f, 0.5f, 0.f));

    RenderCylinder(pPipe->GetColour(), model);

    model = pPipe->GetModel();

    // translate cylinder model to top of pipe
    model = glm::translate(model, glm::vec3(0.f, pPipe->m_BaseHeight, 0.f));
    // scale based on pipe size
    glm::vec3 scale2(pPipe->m_LipRadius, pPipe->m_LipHeight, pPipe->m_LipRadius);
    model = glm::scale(model, scale2);
    // translate cylinder model to be level with ground
    model = glm::translate(model, glm::vec3(0.f, 0.5f, 0.f));

    RenderCylinder(pPipe->GetColour(), model);
}