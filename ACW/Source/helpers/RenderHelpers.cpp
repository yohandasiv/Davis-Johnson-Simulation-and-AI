void OpenGLRenderHelpers::SetupShader()
{
    // write, compile and link some shaders
    std::string vertexShader =
        "#version 330 core\n"
        "layout(location = 0) in vec3 iPosition;\n"
        "layout(location = 1) in vec3 iNormal;\n"

        "uniform mat4 uModel;\n"
        "uniform mat4 uView;\n"
        "uniform mat4 uProjection;\n"

        "out vec3 oNormal;\n"
        "out vec3 oFragmentPosition;\n"

        "void main()\n"
        "{\n"
            "oNormal = mat3(transpose(inverse(uModel))) * iNormal;\n" // calculation of inverse transpose should be on cpu
            "gl_Position = uProjection * uView * uModel * vec4(iPosition, 1.0);\n"
            "oFragmentPosition = vec3(uModel * vec4(iPosition, 1.0));\n"
        "}\n";

    std::string fragmentShader =
        "#version 330 core\n"

        "layout(location = 0) out vec4 oColour;\n"

        "uniform vec3 uColour;\n"
        "uniform vec3 uLightPosition;\n"

        "in vec3 oNormal;\n"
        "in vec3 oFragmentPosition;\n"

        "void main(){\n"
        "   vec3 normal = normalize(oNormal);\n"
        "   vec3 lightDir = normalize(uLightPosition - oFragmentPosition);\n"
        "   float diffuse = max(dot(normal, lightDir), 0.0);\n"
        "   oColour = vec4(diffuse * uColour, 1.0);\n"
        "}\n";

    GLuint vertexShaderID = glCreateShader(GL_VERTEX_SHADER);
    const char* shader = vertexShader.c_str();
    glShaderSource(vertexShaderID, 1, &shader, nullptr);
    glCompileShader(vertexShaderID);
    int result;
    glGetShaderiv(vertexShaderID, GL_COMPILE_STATUS, &result);

    GLuint fragmentShaderID = glCreateShader(GL_FRAGMENT_SHADER);
    shader = fragmentShader.c_str();
    glShaderSource(fragmentShaderID, 1, &shader, nullptr);
    glCompileShader(fragmentShaderID);
    m_ShaderProgramID = glCreateProgram();

    glAttachShader(m_ShaderProgramID, vertexShaderID);
    glAttachShader(m_ShaderProgramID, fragmentShaderID);
    glLinkProgram(m_ShaderProgramID);
    glValidateProgram(m_ShaderProgramID);
    glUseProgram(m_ShaderProgramID);

    m_ColourLocation = glGetUniformLocation(m_ShaderProgramID, "uColour");
    m_ModelLocation = glGetUniformLocation(m_ShaderProgramID, "uModel");
    m_ViewLocation = glGetUniformLocation(m_ShaderProgramID, "uView");
    m_ProjectionLocation = glGetUniformLocation(m_ShaderProgramID, "uProjection");
    m_LightLocation = glGetUniformLocation(m_ShaderProgramID, "uLightPosition");

    glDeleteShader(vertexShaderID);
    glDeleteShader(fragmentShaderID);
}

